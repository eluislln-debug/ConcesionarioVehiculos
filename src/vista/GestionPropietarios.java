    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        
        int fila = tblPropietarios.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un propietario para eliminar.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this,
                "¿Está seguro de eliminar este propietario?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION) {
            modelo.removeRow(fila);
            guardarCambiosCSV();
            JOptionPane.showMessageDialog(this, "Propietario eliminado correctamente.");
            
            // Limpiar campos
            txtId.setText("");
            txtNombre.setText("");
            txtCorreo.setText("");
            txtTelefono.setText("");
            txtDireccion.setText("");
            txtId.setEnabled(true);
        }
    
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblPropietariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPropietariosMouseClicked
        
        
        int fila = tblPropietarios.getSelectedRow();
        if (fila >= 0) {
            txtId.setText(modelo.getValueAt(fila, 0).toString());
            txtNombre.setText(modelo.getValueAt(fila, 1).toString());
            txtCorreo.setText(modelo.getValueAt(fila, 2).toString());
            txtTelefono.setText(modelo.getValueAt(fila, 3).toString());
            txtDireccion.setText(modelo.getValueAt(fila, 4).toString());
            // Deshabilitar la edición del ID para evitar inconsistencias
            txtId.setEnabled(false); 
        }
    
    }//GEN-LAST:event_tblPropietariosMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     int fila = tblPropietarios.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un propietario primero para actualizar.");
            return;
        }
        
   
        if (txtNombre.getText().isEmpty() || txtCorreo.getText().isEmpty() || 
            txtTelefono.getText().isEmpty() || txtDireccion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos deben estar llenos para actualizar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
     
        modelo.setValueAt(txtNombre.getText(), fila, 1);
        modelo.setValueAt(txtCorreo.getText(), fila, 2);
        modelo.setValueAt(txtTelefono.getText(), fila, 3);
        modelo.setValueAt(txtDireccion.getText(), fila, 4);
        
        // 4. Guardar y notificar
        guardarCambiosCSV();
        JOptionPane.showMessageDialog(this, "Propietario actualizado correctamente.");
        
   
        limpiarCampos(); 
    
    }//GEN-LAST:event_jButton1ActionPerformed

