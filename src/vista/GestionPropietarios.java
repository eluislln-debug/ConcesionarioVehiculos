    private void cargarPropietarios() {
        
        modelo = new DefaultTableModel(new String[]{"ID", "Nombre", "Correo", "Telefono", "Direccion"}, 0);
        
    
        File archivo = new File(RUTA);
        if (!archivo.exists()) {
            try {
            
                new File(archivo.getParent()).mkdirs();
                archivo.createNewFile();
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error al crear el archivo de propietarios: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                logger.log(Level.SEVERE, "Error al crear propietarios.csv", e);
                return;
            }
        }
        
        // Carga los datos
        try (BufferedReader br = new BufferedReader(new FileReader(RUTA))) {
            String linea;
            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split(",");
       
                if (datos.length == 5) {
                    modelo.addRow(datos);
                }
            }
            tblPropietarios.setModel(modelo);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al leer propietarios: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            logger.log(Level.SEVERE, "Error al leer propietarios.csv", e);
        }
    }
    
