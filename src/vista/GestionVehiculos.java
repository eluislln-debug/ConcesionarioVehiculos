    private void btnCargarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarImagenActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Seleccionar imagen del veh칤culo");
        fileChooser.setFileFilter(new FileNameExtensionFilter("Im치genes", "jpg", "png", "jpeg"));

        int resultado = fileChooser.showOpenDialog(this);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivo = fileChooser.getSelectedFile();
            String rutaDestino = "imagenes/" + archivo.getName();

            try {
                // Crea la carpeta si no existe
                Files.createDirectories(Paths.get("imagenes"));
                // Copia la imagen dentro de la carpeta del proyecto
                Files.copy(archivo.toPath(), Paths.get(rutaDestino), StandardCopyOption.REPLACE_EXISTING);

                txtImagenes.setText(rutaDestino);
                ImageIcon icon = new ImageIcon(rutaDestino);
                Image imagen = icon.getImage().getScaledInstance(lblImagenVehiculo.getWidth(), lblImagenVehiculo.getHeight(), Image.SCALE_SMOOTH);
                lblImagenVehiculo.setIcon(new ImageIcon(imagen));

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al cargar imagen: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnCargarImagenActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            // Validar campos obligatorios
            if (txtId.getText().trim().isEmpty() ||
                txtMarca.getText().trim().isEmpty() ||
                txtModelo.getText().trim().isEmpty() ||
                txtPrecio.getText().trim().isEmpty() ||
                txtAnio.getText().trim().isEmpty() ||
                txtKilometraje.getText().trim().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.");
                return;
            }

            Vehiculo v = new Vehiculo();
            v.setId(txtId.getText().trim());
            v.setMarca(txtMarca.getText().trim());
            v.setModelo(txtModelo.getText().trim());
            v.setCaracteristicas(txtCaracteristicas.getText().trim());
            v.setDescripcion(txtDescripcion.getText().trim());
            v.setImagenes(txtImagenes.getText().trim());
            v.setPropietario(txtPropietario.getText().trim());

            // 游댳 Precio
            String precioTexto = txtPrecio.getText().trim().replace(",", "");
            try {
                double precio = Double.parseDouble(precioTexto);
                v.setPrecio(precio);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Formato de precio inv치lido. Use solo n칰meros, sin comas ni s칤mbolos.");
                return;
            }

            // 游댳 A침o
            try {
                int anio = Integer.parseInt(txtAnio.getText().trim());
                v.setAnio(anio);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Formato de a침o inv치lido. Use solo n칰meros enteros.");
                return;
            }

            // 游댳 Kilometraje
            String kmTexto = txtKilometraje.getText().trim().replace(",", "");
            try {
                double km = Double.parseDouble(kmTexto);
                v.setKilometraje(km);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Formato de kilometraje inv치lido. Use solo n칰meros, sin comas ni s칤mbolos.");
                return;
            }

            // Agregar a la lista y guardar
            listaVehiculos.add(v);
            dao.guardarVehiculos(listaVehiculos);

            // Actualizar tabla y limpiar campos
            mostrarVehiculosEnTabla();
            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Veh칤culo registrado correctamente.");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

