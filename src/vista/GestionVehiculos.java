    private void mostrarVehiculosEnTabla() {
        String[] columnas = {"ID", "Marca", "Modelo", "Caracter칤sticas", "Precio", "A침o", "Descripci칩n", "Im치genes", "Propietario", "Kilometraje"};
        modeloTabla = new DefaultTableModel(columnas, 0);
        tablaVehiculos.setModel(modeloTabla);

        for (Vehiculo v : listaVehiculos) {
            Object[] fila = {
                v.getId(),
                v.getMarca(),
                v.getModelo(),
                v.getCaracteristicas(),
                v.getPrecio(),
                v.getAnio(),
                v.getDescripcion(),
                v.getImagenes(),
                v.getPropietario(),
                v.getKilometraje()
            };
            modeloTabla.addRow(fila);
        }
    }

