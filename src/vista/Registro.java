    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String cedula = txtCedula.getText().trim();
        String nombre = txtNombre.getText().trim();
        String usuario = txtUsuario.getText().trim();
        String contrasena = new String(txtContrasena.getPassword()).trim();
        String confirmar = new String(txtConfirmarContrasena.getPassword()).trim();
        String telefono = txtTelefono.getText().trim();
        String rol = "cliente";

        lblMensaje.setForeground(java.awt.Color.RED); // por defecto, mensajes de error

        if (cedula.isEmpty() || nombre.isEmpty() || usuario.isEmpty() || contrasena.isEmpty() || confirmar.isEmpty()) {
            lblMensaje.setText("⚠️ Por favor, complete todos los campos obligatorios.");
            return;
        }

        if (!contrasena.equals(confirmar)) {
            lblMensaje.setText("⚠️ Las contraseñas no coinciden.");
            return;
        }

        String ruta = "src/data/usuarios.csv";
        java.io.File archivo = new java.io.File(ruta);

        try {
            if (!archivo.exists()) {
                archivo.getParentFile().mkdirs();
                archivo.createNewFile();
            }
        } catch (java.io.IOException e) {
            lblMensaje.setText("❌ No se pudo crear el archivo: " + e.getMessage());
            return;
        }

        if (usuarioExiste(ruta, cedula, usuario)) {
            lblMensaje.setText("⚠️ El usuario o la cédula ya están registrados.");
            return;
        }

        try (java.io.FileWriter fw = new java.io.FileWriter(ruta, true);
             java.io.BufferedWriter bw = new java.io.BufferedWriter(fw);
             java.io.PrintWriter pw = new java.io.PrintWriter(bw)) {

            pw.println(cedula + "," + nombre + "," + usuario + "," + contrasena + "," + rol + "," + telefono);

            lblMensaje.setForeground(new java.awt.Color(0, 128, 0)); // verde
            lblMensaje.setText("✅ Cliente registrado correctamente.");

            // Limpiar campos
            txtCedula.setText("");
            txtNombre.setText("");
            txtUsuario.setText("");
            txtContrasena.setText("");
            txtConfirmarContrasena.setText("");
            txtTelefono.setText("");

        } catch (java.io.IOException e) {
            lblMensaje.setText("❌ Error al registrar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

