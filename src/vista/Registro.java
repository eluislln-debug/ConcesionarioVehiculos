    private boolean usuarioExiste(String ruta, String cedula, String usuario) {
    try (BufferedReader br = new BufferedReader(new FileReader(ruta))) {
        String linea;
        while ((linea = br.readLine()) != null) {
            String[] datos = linea.split(",");
            if (datos.length >= 3) {
                if (datos[0].equals(cedula) || datos[2].equals(usuario)) {
                    return true;
                }
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    return false;
}
